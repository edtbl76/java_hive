public class UsingEnhancedFor {

    public static void main(String[] args) {

        String[] archangels = {
                "Raguel", "Uriel", "Jophiel", "Haniel", "Jeremiel", "Michael", "Raphael", "Raziel", "Ariel"
        };

        for (String angel : archangels) {
            System.out.println(angel);
        }
    }
}
