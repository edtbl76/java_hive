package com.basics.Operators;

public class BooleanLogicalOperator {

    public static void main(String[] args) {

        int result = 20;

        if (result > 10 && result < 30) {
            System.out.println("True!");
        } else {
            System.out.println("False.");
        }

        if (result > 10 ||  result < 30) {
            System.out.println("True!");
        } else {
            System.out.println("False.");
        }



    }
}
