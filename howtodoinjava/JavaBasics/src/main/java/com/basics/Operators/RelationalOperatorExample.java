package com.basics.Operators;

public class RelationalOperatorExample {

    public static void main(String[] args) {

        int result = 20;

        if (result > 10 ) {
            System.out.println("True");
        } else {
            System.out.println("False");
        }

        boolean isEqual = ( 10 == 20 );
        System.out.println(isEqual);
    }
}
